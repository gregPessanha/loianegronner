package br.com.loiane.cursojava.aula16e17.exercicios;

public class Ex006 {
/**
 * Faça um programa que imprima na tela os números de 1 a 20, um abaixo do outro.
 * @param args
 */
	public static void main(String[] args) {

		for(int i = 1; i <= 20; i++) {
			System.out.print(i + " ");
		}
		
	}

}
